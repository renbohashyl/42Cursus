#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	exp;

	if (fd > 0)
	{
		if (n == -2147483648)
			write(fd, "-2147483648", 11);
		if (n >= -2147483647 && n <= 2147483647)
		{
			if (n < 0)
			{
				write(fd, "-", 1);
				n *= -1;
			}
			if (n > 9)
				ft_putnbr_fd(n / 10, fd);
			exp = (n % 10) + 0x30;
			write(fd, &exp, 1);
		}
	}
}
